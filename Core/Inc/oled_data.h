/**
 * @brief 根据字符查询 6x8 字模数据。
 * @param ch 输入字符。
 * @retval 返回对应字模首地址；未命中时返回 '?' 或空格字模。
 */
static inline const uint8_t *OLED_GetGlyph(char ch)
{
    size_t i;
    char normalized = ch;

    if (normalized >= 'a' && normalized <= 'z') {
        normalized = (char)(normalized - 32);
    }

    for (i = 0; i < (sizeof(OLED_FONT_MAP) / sizeof(OLED_FONT_MAP[0])); ++i) {
        if (OLED_FONT_MAP[i].ch == normalized) {
            return OLED_FONT_MAP[i].bitmap;
        }
    }

    for (i = 0; i < (sizeof(OLED_FONT_MAP) / sizeof(OLED_FONT_MAP[0])); ++i) {
        if (OLED_FONT_MAP[i].ch == '?') {
            return OLED_FONT_MAP[i].bitmap;
        }
    }

    return OLED_FONT_MAP[0].bitmap;
}

